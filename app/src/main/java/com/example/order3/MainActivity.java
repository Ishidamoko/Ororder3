public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        //ViewPagerにAdapterを設定
        ViewPager pager = findViewById(R.id.pager);
        pager.setAdapter(
                new PageAdapter(getSupportFragmentManager()));

        //TabLayoutにViewPagerを紐付け
        TabLayout tabs = findViewById(R.id.tabs);
        tabs.setupWithViewPager(pager);

        //TabLayoutにアイコンを設定
        tabs.getTabAt(0).setIcon(R.drawable.susi);
        tabs.getTabAt(1).setIcon(R.drawable.drink);
        tabs.getTabAt(2).setIcon(R.drawable.side);

    }


    //注文ボタンをクリックしたときに実行されるコード
    public void btClick (View view){
        DialogFragment dialog = new MyDialongFragment();
        dialog.show(getSupportFragmentManager(), "dialog_button");
    }

    //オプションメニューを設置
    public boolean onCreateOptionsMenu(Menu menu) {
        MenuInflater inflater = getMenuInflater();
        inflater.inflate(R.menu.option_menu, menu);
        return true;
    }

    //オプションメニューの項目を選択するとOrderActivityのページへ移動
    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        switch (item.getItemId()) {
            case R.id.item1:
                Intent  intent = new Intent(getApplication(),OrderActivity.class);
                startActivity(intent);
                return true;
        }
        return super.onOptionsItemSelected(item);
    }
}






























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































